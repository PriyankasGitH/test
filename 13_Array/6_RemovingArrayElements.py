print("============= Remove the array elements : with pop =============")
cars = ["Ford", "Volvo", "BMW"]
print(cars, "The array before popping an item")
cars.pop(1)
print(cars, "The array after popping an item")

print("============= Remove the array elements : with remove =============")
cars = ["Ford", "Volvo", "BMW"]
print(cars, "The array before popping an item")
cars.remove("BMW")
print(cars, "The array after popping an item")