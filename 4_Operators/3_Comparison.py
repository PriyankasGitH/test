# This operator is used to compare two values

"""

-- Equal

-- Not equal

-- Greate than

-- Less than

-- Greater than or equal to

-- Less than or equal to

"""

print("========== Comparison operators =============")
x = 5
y = 3

print(x == y, " : Result with comparison operator")
print(x != y, " : Result with not equal to comparison operator")
print(x > y, " : Result with greater than comparison operator")
print(x < y, " : Result with less than comparison operator")
print(x >= y, " : Result with greater than equal to comparison operator")
print(x < y, " : Result with less than equal to comparison operator")
